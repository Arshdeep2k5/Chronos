#[cfg(test)]
mod tests {
    use super::*;
    use serde_json::json;

    #[test]
    fn test_chronos_event_serialization() {
        let event = ChronosEvent::new(
            "ClipboardCopied",
            "User",
            json!({"text": "print('hello')"}),
        );
        let serialized = serde_json::to_string(&event).unwrap();
        let deserialized: ChronosEvent = serde_json::from_str(&serialized).unwrap();
        assert_eq!(event, deserialized);
        assert_eq!(deserialized.schema_version, "1.0");
    }

    #[test]
    fn test_chronos_intent_serialization() {
        let intent = ChronosIntent::new(
            IntentType::Session,
            "Implement Core Runtime",
            95,
            vec!["Rust".to_string(), "Architecture".to_string()],
        );
        let serialized = serde_json::to_string(&intent).unwrap();
        let deserialized: ChronosIntent = serde_json::from_str(&serialized).unwrap();
        assert_eq!(intent, deserialized);
        assert_eq!(deserialized.status, IntentStatus::Active);
    }

    #[test]
    fn test_chronos_capability_serialization() {
        let cap = ChronosCapability::new(
            "GitAdapter",
            CapabilityStatus::Enabled,
            CapabilityHealth::Healthy,
            CapabilityPermission::Granted,
            json!({"version": "2.40.1"}),
        );
        let serialized = serde_json::to_string(&cap).unwrap();
        let deserialized: ChronosCapability = serde_json::from_str(&serialized).unwrap();
        assert_eq!(cap, deserialized);
    }

    #[test]
    fn test_chronos_decision_and_action() {
        let decision = ChronosDecision::new(
            80,
            "Detected completion intent",
            vec!["evt_123".to_string()],
            "NotifyUser",
            json!({"message": "Good job"}),
            None,
        );
        let serialized_dec = serde_json::to_string(&decision).unwrap();
        let deserialized_dec: ChronosDecision = serde_json::from_str(&serialized_dec).unwrap();
        assert_eq!(decision, deserialized_dec);

        let action = ChronosAction::new(
            decision.id.clone(),
            decision.action_type.clone(),
            decision.action_payload.clone(),
        );
        let serialized_act = serde_json::to_string(&action).unwrap();
        let deserialized_act: ChronosAction = serde_json::from_str(&serialized_act).unwrap();
        assert_eq!(action, deserialized_act);
        assert_eq!(action.status, ActionStatus::Pending);
    }

    #[test]
    fn test_chronos_reflection_serialization() {
        let reflection = ChronosReflection::new(
            Some("act_123".to_string()),
            Some("dec_123".to_string()),
            "User accepted recommendation",
            5,
        );
        let serialized = serde_json::to_string(&reflection).unwrap();
        let deserialized: ChronosReflection = serde_json::from_str(&serialized).unwrap();
        assert_eq!(reflection, deserialized);
    }

    #[test]
    fn test_chronos_state_serialization() {
        let state = ChronosState::new(
            vec!["intent_1".to_string()],
            vec!["cap_1".to_string()],
            json!({"environment": "development"}),
        );
        let serialized = serde_json::to_string(&state).unwrap();
        let deserialized: ChronosState = serde_json::from_str(&serialized).unwrap();
        assert_eq!(state, deserialized);
    }
}
