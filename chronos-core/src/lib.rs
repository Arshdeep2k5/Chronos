//! # Chronos Core Runtime
//!
//! This crate defines the canonical runtime objects for the Chronos Personal Context Operating System.
//! It serves as the foundational data contract for the entire architecture. No business logic
//! exists here; this crate exists strictly to define the `Chronos Object Model (COM)`.
//!
//! ## Objects
//! - `ChronosEvent`: The fundamental unit of perception.
//! - `ChronosState`: The aggregated world model.
//! - `ChronosIntent`: The inferred objective (Goal or Session).
//! - `ChronosDecision`: A formal resolution to intervene.
//! - `ChronosAction`: An executable command.
//! - `ChronosReflection`: The learning record.
//! - `ChronosCapability`: System capabilities, permissions, and health.

use chrono::{DateTime, Utc};
use serde::{Deserialize, Serialize};
use uuid::Uuid;

/// Returns the current stable schema version for all objects.
pub const CURRENT_SCHEMA_VERSION: &str = "1.0";

/// Generates a stable unique identifier.
fn generate_id() -> String {
    Uuid::new_v4().to_string()
}

/// # ChronosEvent
/// The fundamental unit of perception. The raw recording of reality.
/// Generated by the Perception Layer (Adapters).
/// Immutable, replayable, and strictly factual.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct ChronosEvent {
    pub id: String,
    pub timestamp: DateTime<Utc>,
    pub schema_version: String,
    pub event_type: String,
    pub source: String,
    pub payload: serde_json::Value,
}

impl ChronosEvent {
    /// Constructs a new `ChronosEvent`.
    pub fn new(event_type: impl Into<String>, source: impl Into<String>, payload: serde_json::Value) -> Self {
        Self {
            id: generate_id(),
            timestamp: Utc::now(),
            schema_version: CURRENT_SCHEMA_VERSION.to_string(),
            event_type: event_type.into(),
            source: source.into(),
            payload,
        }
    }

    /// Constructs a `ChronosEvent` preserving original provenance (ID and Timestamp).
    pub fn new_with_provenance(
        id: impl Into<String>,
        timestamp: DateTime<Utc>,
        event_type: impl Into<String>,
        source: impl Into<String>,
        payload: serde_json::Value,
    ) -> Self {
        Self {
            id: id.into(),
            timestamp,
            schema_version: CURRENT_SCHEMA_VERSION.to_string(),
            event_type: event_type.into(),
            source: source.into(),
            payload,
        }
    }
}

